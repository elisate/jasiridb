import User from '../models/user.models.js'
import extend from 'lodash/extend.js'
import errorHandler from './../helpers/dbErrorHandler.js'
import formidable from 'formidable'
import fs from 'fs'
import path from 'path';


const create = async (req, res) => {
  try {
    // Check if both email and username are provided
    const { email, name } = req.body;
    if (!email || !name) {
      return res.status(400).json({
        error: "Email and username are required.",
      });
    }

    // Ensure case-insensitive check for email and username
    const existingUser = await User.findOne({
      $or: [
        { email: { $regex: `^${email}$`, $options: "i" } }, // Case-insensitive check for email
        { name: { $regex: `^${name}$`, $options: "i" } }, // Case-insensitive check for username
      ],
    });

    if (existingUser) {
      return res.status(400).json({
        error: "User with this email or username already exists.",
      });
    }

    // If no existing user is found, create a new user
    const user = new User(req.body);
    await user.save();

    return res.status(200).json({
      message: "Successfully signed up!",
    });
  } catch (err) {
    console.error(err); // Add logging to debug the error
    return res.status(400).json({
      error: errorHandler.getErrorMessage(err),
    });
  }
};



